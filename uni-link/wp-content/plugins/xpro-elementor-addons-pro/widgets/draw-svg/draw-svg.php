				),
			)
		);

		$this->add_control(
			'svg_bg_color',
			array(
				'label'     => __( 'Background Color', 'xpro-elementor-addons-pro' ),
				'type'      => Controls_Manager::COLOR,
				'selectors' => array(
					'{{WRAPPER}} .xpro-draw-svg-wrapper' => 'background-color: {{VALUE}};',
