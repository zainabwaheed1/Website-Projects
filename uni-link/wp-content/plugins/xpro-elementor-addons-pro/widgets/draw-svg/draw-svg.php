			)
		);

		$this->add_control(
			'reverse',
			array(
				'label'              => __( 'Reverse', 'xpro-elementor-addons-pro' ),
				'description'        => __( 'Direction that the line will be drawn.', 'xpro-elementor-addons-pro' ),
				'type'               => Controls_Manager::SWITCHER,
				'return_value'       => 'yes',
				'frontend_available' => true,
				'render_type'        => 'template',
			)
		);

		$this->add_control(
			'duration',
			array(
				'label'              => __( 'Duration', 'xpro-elementor-addons-pro' ),
				'description'        => __( 'The time to complete the animation of each path.', 'xpro-elementor-addons-pro' ),
